let weather = 'snowy';
switch(weather){
  case 'sunny':
    console.log("It's nice outside!");
    break;
  case 'rainy':
    console.log("It's wet outside!");
    break;
  case 'snowy':
    console.log("Let's build a snowman!");
    break;
  case 'windy':
    console.log("Let's fly a kite!");      
}